package tdtu.finalproject.appsale;
// about item type: laptop, phone show in menu when click button menu in toolbar
public class ItemType {
    public int id;
    public String name;
    public String image;

    public ItemType(int id, String name, String image) {
        this.id = id;
        this.name = name;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
